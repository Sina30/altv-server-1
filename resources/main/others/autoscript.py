def button (list):
    n = len(list)

    for i in range (0, n):
        print('<tr>')
        print('<td>')
        print('<center>')
        print('<button id="' + list[i] + '" class="button button1">' + list[i] + '</button>')
        print('</center>')
        print('</td>')
        print('</tr>')



def img (list):
    n = len(list)

    for i in range (0, n):
        print('<td>')
        print('<center>')
        print('<img src="vehimg/240px-' + list[0] + '.png" width="240" height="140"/>')
        print('</center>')
        print('</td>')

def slider (list):
        n = len(list)

        for i in range (0, n):
                print('<tr>')
                print('    <td>')
                print('    <h2> ' + list[i] + ' </h2>')
                print('    </td>')
                print('    <td>')
                print('        <input id="' + list[i].lower() + '" class="slider" type="range" min="0" max="1" value="0" step="0.01">')
                print('        <h3 id="show' + list[i].lower() + '" style="display:inline"></h3>')
                print('    </td>')
                print('</tr>')

